#include <iostream>

using namespace std;

int main()
{
	int a;
	int b;

	cout << "Enter the first number: ";
	cin >> a;
	cout << "Enter the second number: ";
	cin >> b;

	cout << "Sum is " << a + b << endl;
	cout << "Product is " << a * b << endl;
	cout << "Difference is " << a - b << endl;
	cout << "Quotient is " << a / b << endl;
	
	return 0;
}
